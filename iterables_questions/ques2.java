public class ques2 {
    public static void main(String[] args) {
        // Write a program to find the sum of 1st 10 even natural numbers.

        int i, sum=0;
        for (i=2;i<=20;i=i+2){
            // System.out.println(i);
            sum = sum+i;
        }
        System.out.println(sum);
    }
    
}
