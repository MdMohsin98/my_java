public class types {
    public static void main(String[] args) {
        // There are two types of variables in java - 
        // 1. Premitive types 
        // 2. Non-premitive types or reference
        int a = 111111111;
        long b = 1222112211234214214L;
        float pi = 3.1414F;
        char letter = '7';
        boolean is_programmer = true;
        String sentence = "This is Java programming.";
        System.out.println(sentence.length());
        System.out.println(sentence.charAt(5));
        System.out.println("Different data types in Java");
        System.out.println(a);
        System.out.println(b);
        System.out.println(pi);
        System.out.println(letter); 
        System.out.println(is_programmer);
    }
    
}
