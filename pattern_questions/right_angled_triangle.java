package pattern_questions;

public class right_angled_triangle {
    public static void main(String[] args) {
        // Create a pattern of right angled triangle

        for(int i = 1;i<=10;i++){
            System.out.println();
            for (int j = 1; j<=i; j++){
                System.out.print("* ");
            }

        }
        
    }    
}
