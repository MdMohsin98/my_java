package pattern_questions;

public class half_pyramid_with_numbers {
    public static void main(String[] args) {
        // Half Pyramid with numbers

        for(int i = 1;i<=9;i++){
            System.out.println();
            for (int j = 1; j<=i;j++){
                System.out.print(j+" ");
            }
        }
    }
    
}
